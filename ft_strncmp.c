#include "libft.h"

int	strncmp(const char *s1, const char *s2, size_t n)
{
	while (n)
	{
		if (*s1 > *s2)
			return (1);
		else if (*s1 < *s2)
			return (-1);
		n--;
		s1++;
		s2++;
	}
	return (0);
}
